# Condicionales if elif else

numero = int(input("digite un numero: "))

if numero > 0:
    print("El numero es positivo")

elif numero == 0:
    print("El numero es cero")

else:
    print("El numero es negativo")

'''la identacion se usa para indicar que el codigo despues del condicional pertenece al mismo, \n'
       'a diferenci de otros codigos que usan llaves para indicarlo'''

''' elif es lo mismo que else if '''

print('Fin del programa')
